/**
 * 
 * @param {'p' | 's'} type 
 * @param {number} time 
 * @param {number} depth 
 * @returns 
 */
function wavedistance(type, time, depth) {
  if (type == 'p') return Math.sqrt((pwavespeed*time/1000)**2 + depth**2)
  if (type == 's') return Math.sqrt((swavespeed*time/1000)**2 + depth**2)
}

